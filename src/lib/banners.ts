const BLANK = ['     ', '     ', '     ', '     ', '     '];

/** Render `text` as 5 rows of ASCII-art block letters. */
export function figlet(text: string): string[] {
	const chars = text.toUpperCase().split('');
	const rows = ['', '', '', '', ''];
	for (const ch of chars) {
		const glyph = GLYPHS[ch] ?? BLANK;
		for (let r = 0; r < 5; r++) {
			rows[r] += glyph[r] + ' ';
		}
	}
	return rows.map((r) => r.replace(/\s+$/, ''));
}
